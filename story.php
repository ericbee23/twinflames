    <div class="story">
        <img src="./images/Dicrectors_Vision.jpg" alt="">
        <p>  Twin Flames is more than just a movie; it's a spiritual journey into the complexities of relationships and the alluring influence of peer pressure." My passion for this project comes from a desire to create a memorable experience that goes beyond the limits of traditional narrative. I want the audience to go on a euphoric and psychedelic experience, with emotional highs and lows all happening in one amazing night.

            This film is heavily influenced by Sam Levinson's futuristic style in 'Euphoria.' I hope to portray the same unapologetic raw energy that involves viewers at many levels of human connection. The visual and emotional impact of 'Euphoria' inspired my drive to create a story that not only speaks to the viewers but also leaves an unforgettable mark on their hearts and thoughts.
            
            The music in 'Twin Flames' is vital in defining the story's atmosphere. I chose non-vocal house music that serves as inspiration for the vibrating beats of a house party.
            
            The editing of the film is a rigorous procedure that is carefully executed to replicate the appearance and feel of the plot. I want the smooth flow of the narrative to grip the audience and make them feel as if they are a part of the main characters' terrible night. Pacing, transitions, and visual aspects are all carefully planned to enhance the overall cinematic experience.
              </p>
